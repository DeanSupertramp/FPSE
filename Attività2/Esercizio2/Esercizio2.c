  GPIOA->MODER  &= ~(0x3F << 20); // Clear GPIOA10, 11 e 12 (ultimi due per out), 0x3F = 63 = 	00111111
  GPIOA->MODER |= (0x00 << 20);   // Set Input con [0x00]
  // Pull-Up Mode su pin di input
